package com.src.Model;

public class City {
	private String city;
	private String path;
	private String desc;
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String i) {
		this.path = i;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}

}
